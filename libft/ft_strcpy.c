#include "libft.h"

void	ft_strcpy(char *dst, char *src)
{
	int	i;

	i = 0;
	if(!src)
	{
		src = NULL;
		return ;
	}
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
}
