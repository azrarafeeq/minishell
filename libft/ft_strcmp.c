#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int		i;

	i = 0;
	if (s1[0] == '\0' && (s2[0] == '\n' || s2[0] == '\n'))
		return (0);
	while (s1[i] && s2[i] && s1[i] == s2[i])
		i++;
	if (s1[i] == '\0' && s2[i] == '\n')
		return (0);
	return (s1[i] - s2[i]);
}
